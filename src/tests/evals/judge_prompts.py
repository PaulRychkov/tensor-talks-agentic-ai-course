"""LLM-as-judge prompts for subjective quality metrics (§10.4).

These prompts are used to evaluate qualities that cannot be checked
deterministically: hint relevance, report quality, response tone.
"""

HINT_JUDGE_PROMPT = """\
Ты — эксперт по оценке качества AI-интервьюера.
Оцени качество подсказки, которую агент дал кандидату.

Вопрос интервью: {question}
Ответ кандидата: {answer}
Подсказка агента: {hint}
Теория по теме: {theory_excerpt}

Оцени по шкале 0.0-1.0:
1. Релевантность (подсказка относится к вопросу и слабому месту ответа)
2. Полезность (подсказка помогает кандидату, не даёт прямой ответ)
3. Корректность (подсказка фактически верна)

Ответ СТРОГО в формате JSON:
{{"relevance": <0.0-1.0>, "usefulness": <0.0-1.0>, "correctness": <0.0-1.0>, "overall": <0.0-1.0>}}
"""

